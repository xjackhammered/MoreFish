def analogToPH(analog, calibration = 0):
    '''
    This equation is generated by two point calibration.
    The details of this equation can be found in hardware documetation
    '''
    R1 = 10 # 10k ohm
    R2 = 22 # 22k ohm
    Aref = 3.3  # Analog reference voltage
    b = 10  # Analog Resolution bits
    test_pH = 7.1   # pH value tested
    test_pH_terminal_voltage = 2.52 # Voltage at the TLC4502C output
    m = 2.4/0.28    # Comes from two point calibration
    
    pH = m * (test_pH_terminal_voltage - analog * ((R1 + R2)/R2) * (Aref/(2**b - 1))) + test_pH + calibration
    return pH

def analogToTemperature(analog, calibration = 73):
    import math
    RTO = 10000  # Ω
    B = 3900  # K
    VCC = 5  # Supply voltage
    R = 10000  # R=10KΩ
    T0 = 25 + 273.15
    
    R1 = 10 #divider
    R2 = 22 #divider
    Aref = 3.3
    b = 10 #reolution
    
    analog = analog * Aref/5 * (R1 + R2)/R2
    
    VRT = (analog + calibration) * VCC / (2**b - 1) # Conversion to voltage
    VR = VCC - VRT
    RT = VRT / (VR / R)  # Resistance of RT
    
    ln = math.log(RT / RTO)
    temperature_kelvin = (1 / ((ln / B) + (1 / T0)))  # Temperature from thermistor

    temperature_celsiuus = temperature_kelvin - 273.15  # Conversion to Celsius
    
    return temperature_celsiuus

