def temperature_value_calculation(temperature_value, temperature_calibration_value=0):
    # temperature equation for D8020ND-1.0
    
    calibration_val = float(temperature_calibration_value)
    RTO = 10000  # Ω
    B = 3900  # K
    VCC = 5  # Supply voltage
    R = 10000  # R=10KΩ
    T0 = 25 + 273.15
    VRT = (5.00 / 1023.00) * (temperature_value + calibration_val)  # Conversion to voltage
    VR = VCC - VRT
    RT = VRT / (VR / R)  # Resistance of RT

    ln = math.log(RT / RTO)
    TX = (1 / ((ln / B) + (1 / T0)))  # Temperature from thermistor

    TX = TX - 273.15  # Conversion to Celsius

    temp_round_result = round(TX, 5)
    return temp_round_result

        
