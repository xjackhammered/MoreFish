def analogToTDS(analog, calibration = 20, temperature = 25, analog_const:int=1023):
    x = (analog_const - analog)*3.3
    averageVoltage = x*3.3/1023*32/22
    compensationCoefficient = 1.0+0.02*(temperature-25.0)
    compensationVoltage = averageVoltage/compensationCoefficient
    tds = (133.42*compensationVoltage**3 - 255.86*compensationVoltage**2 + 857.39*compensationVoltage)/(2*calibration)
    return tds


def get_battery_voltage(analog_reading):
    VOLTAGE_REFERENCE = 3.3
    R1 = 10 * 1000
    R5 = 10 * 1000
    ERROR_VOLTAGE = 0

    gpio_voltage = (float(analog_reading) / 1023) * VOLTAGE_REFERENCE
    battery_voltage = ((R1 + R5) / R5) * (gpio_voltage + ERROR_VOLTAGE)

    return round(battery_voltage,2)

